package java;

/* 
 * Given two integers num1 and num2, return the sum of the two integers.
 * 
 * Constraints:
 *  -100 <= num1, num2 <= 100
 * 
 * Time complexity: O(1)
 * Space complexity: O(1)
 * 
 * Test:
 * 1. normal cases
 * 2. null input
 * 3. edge cases: num1/num2 = -100, num1/num2 = 100
 */
class Solution {
    public int sum(int num1, int num2) {
        return num1 + num2;
    }
}
